import type { FoldStep, OrigamiModel } from '../engine/types';

/**
 * ハート / Heart(全3工程)
 * 定番の「角落とし」ハート。正方形(辺が水平)の角と上辺中央を後ろへ折るだけで
 * 作れる1枚折り。裏(白)はすべて後ろを向くので、正面は赤いハートに見える。
 *
 *   ❶ てっぺんの中央をつまんで後ろへたくし込み、山を2つに分ける(くぼみ)
 *   ❷ 上の左右の角を後ろへ折る(2つの山を丸く)
 *   ❸ 下の左右の角を後ろへ折る(すそをとがらせる)
 *
 * くぼみの表現(一度保留になった問題の解):
 * くさび(上辺の 4-5 と くぼみ底 6 の逆三角)を上辺の水平線 4-5 を軸に後ろへ
 * 折ると、上辺より上に紙が無いため頂点が上へ跳ね上がって失敗する(旧実装)。
 * 実物は「中央をつまんで奥へたくし込む」うさぎの耳折りだが、エンジンでは
 * くさびを中央線で左右に分割し、上辺中央の頂点(0,1)を左右で複製(15/16)して、
 * それぞれを斜めの折り線 4-6 / 5-6 で後ろへ折る「小さな角折り2つ」に置き換えた。
 * 複製した中央頂点は (∓0.47, 0.76) に着地し、山のフラップの裏に隠れる。
 * 物理的には中央線でつながったままの紙を切り離しているが、両片とも裏(z<0)へ
 * 回るため正面からは見えず、シルエットは 7→4→6→5→9 のV字くぼみになる。
 *
 * 展開図は辺が水平の正方形:左上(-1,1) 右上(1,1) 右下(1,-1) 左下(-1,-1)。
 * 上辺の左右=2つの山、上辺中央=くぼみ、下辺中央(0,-1)=すその先。
 * すべて山折り(後ろ折り)なので2層の色管理は不要。表=赤。
 */

const V: [number, number][] = [
  [-1, 1], //  0: 左上の角(❷で後ろへ=左の山)
  [1, 1], //  1: 右上の角(❷で後ろへ=右の山)
  [1, -1], //  2: 右下の角(❸で後ろへ)
  [-1, -1], //  3: 左下の角(❸で後ろへ)
  // くぼみ(上辺中央のくさびを左右に分けて後ろへ)
  [-0.3, 1], //  4: くぼみ折り線・左(上辺上)
  [0.3, 1], //  5: くぼみ折り線・右(上辺上)
  [0, 0.42], //  6: くぼみの底(内部点。両方の折り線の共有端=動かない)
  // 上の山を丸く(左上・右上の角を後ろへ)
  [-0.62, 1], //  7: 左山折り線・上(上辺上)
  [-1, 0.55], //  8: 左山折り線・横(左辺上)
  [0.62, 1], //  9: 右山折り線・上(上辺上)
  [1, 0.55], // 10: 右山折り線・横(右辺上)
  // 下のすそを絞る(左下・右下の角を後ろへ)
  [-1, -0.15], // 11: 左すそ折り線・横(左辺上)
  [-0.12, -1], // 12: 左すそ折り線・下(下辺上)
  [1, -0.15], // 13: 右すそ折り線・横(右辺上)
  [0.12, -1], // 14: 右すそ折り線・下(下辺上)
  // 15/16: 上辺中央 (0,1) の左右複製。くさびを2つの角折りに分けるための層分け
  [0, 1], // 15: くさび左半分の先
  [0, 1], // 16: くさび右半分の先
  [0, -1], // 17: 下辺の中点(すその先)。中央面を凸に分割するための頂点
];

const F: number[][] = [
  [4, 6, 15], // くさび左半分(❶で折り線 4-6 から後ろへ)
  [16, 6, 5], // くさび右半分(❶で折り線 5-6 から後ろへ)
  [0, 7, 8], // 左の山の角(❷で後ろへ)
  [1, 10, 9], // 右の山の角(❷で後ろへ)
  [3, 11, 12], // 左すその角(❸で後ろへ)
  [2, 14, 13], // 右すその角(❸で後ろへ)
  // 中央(残り)。くぼみ(6)が凹(reflex)頂点になるため、1枚の11角形にすると
  // レンダラの扇形分割(先頭頂点から放射)が破綻し、くぼみの上に裏向きの
  // 偽三角形が出る(実測: 上部に白いフラップ状の面が浮いて見えた)。
  // くぼみ底(6)から下辺中点(17)へ縦に切って2つの星形多角形に分け、
  // どちらも 6 を先頭にして扇の根元にする(6 からは全頂点が見通せる)
  [6, 4, 7, 8, 11, 12, 17], // 中央・左
  [6, 17, 14, 13, 10, 9, 5], // 中央・右
];

/** 反時計回り(表=+z)へ揃える */
function orient(f: number[]): number[] {
  let a = 0;
  for (let i = 0; i < f.length; i++) {
    const [x1, y1] = V[f[i]];
    const [x2, y2] = V[f[(i + 1) % f.length]];
    a += x1 * y2 - x2 * y1;
  }
  return a >= 0 ? f : [...f].reverse();
}

