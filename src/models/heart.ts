import type { FoldStep, OrigamiModel } from '../engine/types';

/**
 * ハート / Heart(全5工程)
 * https://www.origami-club.com/valentine/easyheart/easyheart/ の折り図に忠実な
 * 組み立て型のハート。白い面を上にして始め、折り返しの裏(朱)が表に出て
 * ハートの色になる。
 *
 *   ❶ 縦半分に折って折り目をつけ、開いて戻す(2工程)
 *   ❷ 上の角を、まんなかへ折り下げる
 *   ❸ 下の角を、上のふちまで大きく折り上げる
 *   ❹ 左右のはしを、斜め上へ折り上げる(同時) → 2つの山ができ、
 *      山のあいだに「くぼみ」が自然に生まれる
 *
 * 旧実装(角落とし型: 赤い正方形の角と上辺中央を後ろへ折るだけ)は原典と
 * 別物だったため、この折り図に合わせて全面的に作り直した。
 * くぼみは折りの構造から自然に生まれる(❹で折り上げた左右の翼の内側の縁が
 * 中央線 x=0 でぴったり突き合い、翼の山 (±0.25,0.75) のあいだが
 * V字 (0,0.5) に凹む)。旧実装のような特別な操作は不要。
 *
 * 展開図はひし形の正方形:上(0,1) 右(1,0) 下(0,-1) 左(-1,0)。
 *
 * 幾何の要点(すべて厳密値):
 * - ❷の折り線 y=0.5 ((∓0.5,0.5))。上の角(0,1)は中心(0,0)へ
 * - ❸の折り線 y=-0.25 ((∓0.75,-0.25))。下の角(0,-1)は(0,0.5)へ=上のふちに一致
 * - ❹の折り線は (0,-0.25) から45°で斜辺へ((∓0.625,0.375))。
 *   左の角(-1,0)は(-0.25,0.75)へ=左の山。(-0.75,-0.25)は(0,0.5)へ=くぼみの底。
 *   ❹の折り線は❸で折り上げたフラップを横切るため、フラップ側にも折り線を
 *   事前に埋め込む(❸の折り線 y=-0.25 で鏡映した (∓0.375,-0.625)-(0,-0.25))。
 *   フラップの角は共有頂点(8/9=❸の折り線上)を介して翼と一緒に回る
 * - ❹の折り線と❷のフラップの縁(y=-x)は平行(オフセット0.25)で交差しない
 *
 * 原典の❺❻(左右の角と山の先を後ろへ折って丸める)は多層折りになるため省略。
 * 角ばったハートだが、山2つ・くぼみ・下のとがりは原典どおり。
 */

const V: [number, number][] = [
  [0, 1], //  0: 上の角(❷でまんなかへ)
  [1, 0], //  1: 右の角(❹で右の山になる)
  [0, -1], //  2: 下の角(❸で上のふちへ)
  [-1, 0], //  3: 左の角(❹で左の山になる)
  [-0.5, 0.5], //  4: ❷折り線・左(左上辺上)
  [0.5, 0.5], //  5: ❷折り線・右(右上辺上)
  [0, 0.5], //  6: ❷折り線の中点(x=0)
  [0, 0], //  7: 中心(❷で上の角が来る場所)
  [-0.75, -0.25], //  8: ❸折り線・左(左下辺上。❹で翼と一緒に回る共有点)
  [0.75, -0.25], //  9: ❸折り線・右(右下辺上)
  [0, -0.25], // 10: ❸折り線の中点(x=0。❹の両折り線の内側の端でもある)
  [-0.625, 0.375], // 11: ❹折り線・左外(左上辺上)
  [0.625, 0.375], // 12: ❹折り線・右外(右上辺上)
  [-0.375, -0.625], // 13: ❹折り線の❸フラップ側・左(左下辺上)
  [0.375, -0.625], // 14: ❹折り線の❸フラップ側・右(右下辺上)
  // 15〜20: ❸フラップの左右の角の専用複製。❹で翼と一緒に回った角は、実物では
  // 翼の下に隠れる(翼が外側を回って上になる)が、エンジンでは同軸回転のため
  // 角が翼の上に残り、2回反転で白い面を見せてしまう(実測 z=0.035-0.045 > 翼)。
  // 面の頂点をすべて専用複製にして、❹の後に z 補正で翼の下へ沈める
  [-0.75, -0.25], // 15: 8 の複製(左角)
  [-0.375, -0.625], // 16: 13 の複製(左角)
  [0, -0.25], // 17: 10 の複製(左角)
  [0.75, -0.25], // 18: 9 の複製(右角)
  [0.375, -0.625], // 19: 14 の複製(右角)
  [0, -0.25], // 20: 10 の複製(右角)
];

const F: number[][] = [
  // ❷のフラップ(上の角。x=0 で分割)
  [0, 6, 4],
  [0, 5, 6],
  // 本体の中央(❹の折り線と❷の折り線のあいだ。x=0 で分割)
  [10, 11, 4, 6],
  [10, 6, 5, 12],
  // 本体の翼(❹で折り上がる。左右)
  [11, 3, 8, 10],
  [12, 10, 9, 1],
  // ❸のフラップ(下の角。❹の折り線の鏡映と x=0 で分割)
  [15, 16, 17], // 左の角(専用複製。❹で翼と一緒に回る)
  [10, 13, 2, 14], // 中央(くぼみの底まで届く)
  [18, 20, 19], // 右の角(専用複製)
];

/** 反時計回り(表=+z)へ揃える */
function orient(f: number[]): number[] {
  let a = 0;
  for (let i = 0; i < f.length; i++) {
    const [x1, y1] = V[f[i]];
    const [x2, y2] = V[f[(i + 1) % f.length]];
    a += x1 * y2 - x2 * y1;
  }
  return a >= 0 ? f : [...f].reverse();
}

